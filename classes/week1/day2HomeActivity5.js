//example of arithmetic operators for calculations
let add = 1; add + 1;
let substract = 10 - 5;
let multiply = 10 * 10;
let exponentiation = 2 ** 10;
let division = 100 / 2;
let modulus = 12 % 5; 
let increment = 0; increment++;
let decrement = 1; decrement--;

console.log(add);
console.log(substract);
console.log(multiply);
console.log(exponentiation);
console.log(division);
console.log(modulus);
console.log(increment);
console.log(decrement);

//example of assignment operators to store values

name = "MyName";
dogs = 1; dogs *= 2;
pizzaSlices = 1; pizzaSlices += 1;
people = 10; people /= 2;
apples = 2; apples -= 1;
cash = 10; cash ++;
tickets = 2; tickets --;

console.log(name);
console.log(dogs);
console.log(pizzaSlices);
console.log(people);
console.log(apples);
console.log(cash);
console.log(tickets);
