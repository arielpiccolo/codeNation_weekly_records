
// loops or control-flow system



// the for loop 


let str = '';
for (let i = 0; i < 9; i++) {
    str = str + i;
}
console.log(str);




let number = 0;
while (number < 5) {
  number++;
}
console.log(number);




 


let result = '';
let i = 0;
do {
  i = i + 1;
  result = result + i;
} while (i < 5);
console.log(result);

